    cout << "Ваш вибір: ";
    cin >> choice;

    switch (choice) {
        case 1: task_if12(); break;
        case 2: task_geom32(); break;
        case 3: task_area_perimeter32(); break;
        default: cout << "Неправильний вибір! Спробуйте ще раз.\n"; break;
    }
